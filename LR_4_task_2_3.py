import numpy as np
from sklearn import linear_model
from sklearn.preprocessing import PolynomialFeatures
import os

# --- АВТОМАТИЧНЕ ВИЗНАЧЕННЯ ШЛЯХУ ---
# Скрипт сам знайде папку, у якій він лежить
поточна_директорія = os.path.dirname(os.path.abspath(__file__))
вхідний_файл = os.path.join(поточна_директорія, 'data_multivar_regr.txt')

# Перевірка: якщо файлу немає, виводимо зрозуміле повідомлення
if not os.path.exists(вхідний_файл):
    print(f"ПОМИЛКА: Файл не знайдено за адресою: {вхідний_файл}")
    print("Переконайтеся, що файл .txt лежить у тій самій папці, що і цей скрипт.")
    exit()

# Завантаження даних
дані = np.loadtxt(вхідний_файл, delimiter=',')
X, y = дані[:, :-1], дані[:, -1]

# Лінійна регресія
лінійний_регресор = linear_model.LinearRegression()
лінійний_регресор.fit(X, y)

# Поліноміальна регресія 10-го ступеня
ступінь = 10
поліном = PolynomialFeatures(degree=ступінь)
X_поліном = поліном.fit_transform(X)

поліноміальний_регресор = linear_model.LinearRegression()
поліноміальний_регресор.fit(X_поліном, y)

# Прогноз для конкретної точки
точка_даних = [[7.75, 6.35, 5.56]]

print(f"--- Прогноз для точки {точка_даних[0]} ---")

# Прогноз лінійної моделі
прогноз_лін = лінійний_регресор.predict(точка_даних)[0]
print("Лінійна регресія:      ", round(прогноз_лін, 4))

# Прогноз поліноміальної моделі
прогноз_полі = поліноміальний_регресор.predict(поліном.transform(точка_даних))[0]
print(f"Поліноміальна (ступ. {ступінь}):", round(прогноз_полі, 4))